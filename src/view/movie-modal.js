export default class MovieModal {
  constructor(movie) {
    this._movie = movie;

    this._element = null;
  }

  getTemplate() {

    return createMovieModalTemplate(this._movie);
  }

  getElement() {

    if (!this._element) {
      this._element = createElement(this.getTemplate());
    }

    return this._element;
  }

  removeElement() {
    this._element = null;
  }
}
